class ACmix_Align(nn.Module):
    def __init__(self, in_planes=128, out_planes=128, kernel_att=7, head=4, kernel_conv=3, reduction=16, stride=1, dilation=1, act="silu"):
        super(ACmix_Align, self).__init__()
        self.in_planes = in_planes
        self.out_planes = out_planes
        self.head = head
        self.kernel_att = kernel_att
        self.kernel_conv = kernel_conv
        self.stride = stride
        self.dilation = dilation
        self.reduction = reduction
        self.head_dim = self.out_planes // self.head
        self.x_qkv_conv = Align_qkv(self.in_planes, self.out_planes)
        self.y_qkv_conv = Align_qkv(self.in_planes, self.out_planes)
        self.x_re_conv = nn.Conv2d(self.out_planes, int(self.out_planes / self.reduction), kernel_size=1)
        self.y_re_conv = nn.Conv2d(self.out_planes, int(self.out_planes / self.reduction), kernel_size=1)
        self.v_conv = nn.Conv2d(int(self.out_planes / self.reduction) * 2, out_planes, kernel_size = 1)
        self.conv_p = nn.Conv2d(2, self.head_dim, kernel_size=1)

        self.padding_att = (self.dilation * (self.kernel_att - 1) + 1) // 2
        self.pad_att = torch.nn.ReflectionPad2d(self.padding_att)
        self.unfold = nn.Unfold(kernel_size=self.kernel_att, padding=0, stride=self.stride)
        self.softmax = torch.nn.Softmax(dim=1)

        self.x_fc = nn.Conv2d(3 * self.head, self.kernel_conv * self.kernel_conv, kernel_size=1, bias=False)
        self.y_fc = nn.Conv2d(3 * self.head, self.kernel_conv * self.kernel_conv, kernel_size=1, bias=False)
        self.x_dep_conv = nn.Conv2d(self.kernel_conv * self.kernel_conv * self.head_dim, self.out_planes,
                                    kernel_size=self.kernel_conv, bias=True, groups=self.head_dim, padding=1,
                                    stride=stride)
        self.y_dep_conv = nn.Conv2d(self.kernel_conv * self.kernel_conv * self.head_dim, self.out_planes,
                                    kernel_size=self.kernel_conv, bias=True, groups=self.head_dim, padding=1,
                                    stride=stride)

        self.bn = nn.BatchNorm2d(self.out_planes)
        self.act = get_activation(act, inplace=True)
        self.reset_parameters()

    def reset_parameters(self):
        kernel = torch.zeros(self.kernel_conv * self.kernel_conv, self.kernel_conv, self.kernel_conv)
        for i in range(self.kernel_conv * self.kernel_conv):
            kernel[i, i // self.kernel_conv, i % self.kernel_conv] = 1.
        kernel = kernel.squeeze(0).repeat(self.out_planes, 1, 1, 1)
        self.x_dep_conv.weight = nn.Parameter(data=kernel, requires_grad=True)
        self.x_dep_conv.bias = init_rate_0(self.x_dep_conv.bias)
        self.y_dep_conv.weight = nn.Parameter(data=kernel, requires_grad=True)
        self.y_dep_conv.bias = init_rate_0(self.y_dep_conv.bias)

    def forward(self, x, y):
        q_x, k_x, v_x = self.x_qkv_conv(x)
        q_y, k_y, v_y = self.y_qkv_conv(y)
        scaling = float(self.head_dim) ** -0.5
        assert q_x.shape == q_y.shape
        b, c, h, w = q_x.shape
        h_out, w_out = h // self.stride, w // self.stride

        # ### att
        # ## positional encoding
        pe_x = self.conv_p(position(h, w, x.is_cuda))
        pe_y = self.conv_p(position(h, w, y.is_cuda))

        q_x_att = q_x.view(b * self.head, self.head_dim, h, w) * scaling
        q_y_att = q_y.view(b * self.head, self.head_dim, h, w) * scaling
        re_x = self.x_re_conv(x)
        re_y = self.y_re_conv(y)
        v = self.v_conv(torch.cat((re_x, re_y), dim=1))
        k_x_att = k_x.view(b * self.head, self.head_dim, h, w)
        k_y_att = k_y.view(b * self.head, self.head_dim, h, w)
        v_att = v.view(b * self.head, self.head_dim, h, w)

        if self.stride > 1:
            q_x_att = stride(q_x_att, self.stride)
            q_y_att = stride(q_y_att, self.stride)
            q_x_pe = stride(pe_x, self.stride)
            q_y_pe = stride(pe_y, self.stride)
        else:
            q_x_pe = pe_x
            q_y_pe = pe_y

        unfold_k_x = self.unfold(self.pad_att(k_x_att)).view(b * self.head, self.head_dim,
                                                         self.kernel_att * self.kernel_att, h_out,
                                                         w_out)  # b*head, head_dim, k_att^2, h_out, w_out
        unfold_k_y = self.unfold(self.pad_att(k_y_att)).view(b * self.head, self.head_dim,
                                                             self.kernel_att * self.kernel_att, h_out,
                                                             w_out)  # b*head, head_dim, k_att^2, h_out, w_out
        unfold_x_rpe = self.unfold(self.pad_att(pe_x)).view(1, self.head_dim, self.kernel_att * self.kernel_att, h_out,
                                                        w_out)  # 1, head_dim, k_att^2, h_out, w_out
        unfold_y_rpe = self.unfold(self.pad_att(pe_y)).view(1, self.head_dim, self.kernel_att * self.kernel_att, h_out,
                                                        w_out)

        x_att = (q_x_att.unsqueeze(2) * (unfold_k_x + q_x_pe.unsqueeze(2) - unfold_x_rpe)).sum(
            1)  # (b*head, head_dim, 1, h_out, w_out) * (b*head, head_dim, k_att^2, h_out, w_out) -> (b*head, k_att^2, h_out, w_out)
        x_att = self.softmax(x_att)

        y_att = (q_y_att.unsqueeze(2) * (unfold_k_y + q_y_pe.unsqueeze(2) - unfold_y_rpe)).sum(
            1)  # (b*head, head_dim, 1, h_out, w_out) * (b*head, head_dim, k_att^2, h_out, w_out) -> (b*head, k_att^2, h_out, w_out)
        y_att = self.softmax(y_att)

        out_att = self.unfold(self.pad_att(v_att)).view(b * self.head, self.head_dim, self.kernel_att * self.kernel_att,
                                                        h_out, w_out)
        x_out_att = (x_att.unsqueeze(1) * out_att).sum(2).view(b, self.out_planes, h_out, w_out)
        y_out_att = (y_att.unsqueeze(1) * out_att).sum(2).view(b, self.out_planes, h_out, w_out)

        ## conv
        x_f_all = self.x_fc(torch.cat(
            [q_x.view(b, self.head, self.head_dim, h * w), k_x.view(b, self.head, self.head_dim, h * w),
             v_x.view(b, self.head, self.head_dim, h * w)], 1))
        x_f_conv = x_f_all.permute(0, 2, 1, 3).reshape(x.shape[0], -1, x.shape[-2], x.shape[-1])
        y_f_all = self.y_fc(torch.cat(
            [q_y.view(b, self.head, self.head_dim, h * w), k_y.view(b, self.head, self.head_dim, h * w),
             v_y.view(b, self.head, self.head_dim, h * w)], 1))
        y_f_conv = y_f_all.permute(0, 2, 1, 3).reshape(x.shape[0], -1, x.shape[-2], x.shape[-1])
        x_out_conv = self.x_dep_conv(x_f_conv)
        y_out_conv = self.y_dep_conv(y_f_conv)

        return self.act(self.bn(x_out_att * x_out_conv)),\
               self.act(self.bn(y_out_att * y_out_conv))
        # return self.act(self.bn((self.x_rate1 * x_out_att) * (self.x_rate2 * x_out_conv))),\
        #        self.act(self.bn((self.y_rate1 * y_out_att) * (self.y_rate2 * y_out_conv)))


class ACAttn(nn.Module):
    def __init__(self, in_planes, out_planes, kernel_att=7, head=1, kernel_conv=3, stride=1, dilation=1, act="silu"):
        super(ACAttn, self).__init__()
        self.in_planes = in_planes
        self.out_planes = out_planes
        self.head = head                       # num_head
        self.kernel_att = kernel_att
        self.kernel_conv = kernel_conv
        self.stride = stride
        self.dilation = dilation

        self.head_dim = self.out_planes // self.head        #per_head_dim

        self.conv1 = nn.Conv2d(in_planes, out_planes, kernel_size=1)
        self.conv2 = nn.Conv2d(in_planes, out_planes, kernel_size=1)
        self.conv3 = nn.Conv2d(in_planes, out_planes, kernel_size=1)
        self.conv_p = nn.Conv2d(2, self.head_dim, kernel_size=1)

        self.padding_att = (self.dilation * (self.kernel_att - 1) + 1) // 2
        self.pad_att = torch.nn.ReflectionPad2d(self.padding_att)
        self.unfold = nn.Unfold(kernel_size=self.kernel_att, padding=0, stride=self.stride)
        self.softmax = torch.nn.Softmax(dim=1)

        self.fc = nn.Conv2d(3 * self.head, self.kernel_conv * self.kernel_conv, kernel_size=1, bias=False)
        self.dep_conv = nn.Conv2d(self.kernel_conv * self.kernel_conv * self.head_dim, out_planes,
                                  kernel_size=self.kernel_conv, bias=True, groups=self.head_dim, padding=1,
                                  stride=stride)

        self.bn = nn.BatchNorm2d(self.out_planes)
        self.act = get_activation(act, inplace=True)
        self.reset_parameters()

    def reset_parameters(self):
        kernel = torch.zeros(self.kernel_conv * self.kernel_conv, self.kernel_conv, self.kernel_conv)
        for i in range(self.kernel_conv * self.kernel_conv):
            kernel[i, i // self.kernel_conv, i % self.kernel_conv] = 1.
        kernel = kernel.squeeze(0).repeat(self.out_planes, 1, 1, 1)
        self.dep_conv.weight = nn.Parameter(data=kernel, requires_grad=True)
        self.dep_conv.bias = init_rate_0(self.dep_conv.bias)

    def forward(self, x):
        q, k, v= self.conv1(x), self.conv2(x), self.conv3(x)
        scaling = float(self.head_dim) ** -0.5
        b, c, h, w = q.shape
        h_out, w_out = h // self.stride, w // self.stride

        # ### att
        # ## positional encoding
        pe = self.conv_p(position(h, w, x.is_cuda))

        q_att = q.view(b * self.head, self.head_dim, h, w) * scaling
        k_att = k.view(b * self.head, self.head_dim, h, w)
        v_att = v.view(b * self.head, self.head_dim, h, w)

        if self.stride > 1:
            q_att = stride(q_att, self.stride)
            q_pe = stride(pe, self.stride)
        else:
            q_pe = pe

        unfold_k = self.unfold(self.pad_att(k_att)).view(b * self.head, self.head_dim,
                                                         self.kernel_att * self.kernel_att, h_out,
                                                         w_out)  # b*head, head_dim, k_att^2, h_out, w_out
        unfold_rpe = self.unfold(self.pad_att(pe)).view(1, self.head_dim, self.kernel_att * self.kernel_att, h_out,
                                                        w_out)  # 1, head_dim, k_att^2, h_out, w_out

        att = (q_att.unsqueeze(2) * (unfold_k + q_pe.unsqueeze(2) - unfold_rpe)).sum(
            1)  # (b*head, head_dim, 1, h_out, w_out) * (b*head, head_dim, k_att^2, h_out, w_out) -> (b*head, k_att^2, h_out, w_out)
        att = self.softmax(att)

        out_att = self.unfold(self.pad_att(v_att)).view(b * self.head, self.head_dim, self.kernel_att * self.kernel_att,
                                                        h_out, w_out)
        out_att = (att.unsqueeze(1) * out_att).sum(2).view(b, self.out_planes, h_out, w_out)

        ## conv
        f_all = self.fc(torch.cat(
            [q.view(b, self.head, self.head_dim, h * w), k.view(b, self.head, self.head_dim, h * w),
             v.view(b, self.head, self.head_dim, h * w)], 1))
        f_conv = f_all.permute(0, 2, 1, 3).reshape(x.shape[0], -1, x.shape[-2], x.shape[-1])

        out_conv = self.dep_conv(f_conv)

        return self.act(self.bn(out_att * out_conv))


class ConvBNLayer(nn.Module):

    def __init__(self,
                 ch_in,
                 ch_out,
                 filter_size=3,
                 stride=1,
                 groups=1,
                 padding=0,
                 act=None):
        super(ConvBNLayer, self).__init__()
        self.conv = nn.Conv2d(
            in_channels=ch_in,
            out_channels=ch_out,
            kernel_size=filter_size,
            stride=stride,
            padding=padding,
            groups=groups,
            bias=False
        )
        self.bn = nn.BatchNorm2d(ch_out, )
        self.act = get_activation(act, inplace=True)

    def forward(self, x):
        x = self.conv(x)
        x = self.bn(x)
        x = self.act(x)

        return x
